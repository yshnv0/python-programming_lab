sq_area=lambda x:(x*x)
re_area=lambda x,y:(x*y)
tr_area=lambda x,y:(0.5*x*y)
x=int(input("Enter the side of the square "))
a=int(input("Enter the length of the rectangle "))
b=int(input("Enter the breadth of the rectangle "))
p=int(input("Enter the base of the triangle "))
q=int(input("Enter the height of the triangle "))
print("Area of Square= ",sq_area(x))
print("Area of Rectangle= ",re_area(a,b))
print("Area of Triangle= ",tr_area(p,q))
